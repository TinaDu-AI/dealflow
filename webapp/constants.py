def get_keywords_for_tracks(track_list: list[dict]) -> list[str]:
    """
    给定用户的赛道列表（[{track_main, track_sub}, ...]），
    返回去重后的所有关键词列表，用于 Layer A 搜索。
    """
    result: list[str] = []
    for t in track_list:
        main = t.get("track_main", "")
        sub = t.get("track_sub", "")
        kws = TRACK_KEYWORDS.get(main, {}).get(sub, [])
        for kw in kws:
            if kw not in result:
                result.append(kw)
    return result


def get_all_keywords() -> list[str]:
    """返回所有赛道的所有关键词（去重），用于全量内容召回。"""
    result: list[str] = []
    for main_dict in TRACK_KEYWORDS.values():
        for kws in main_dict.values():
            for kw in kws:
                if kw not in result:
                    result.append(kw)
    return result
